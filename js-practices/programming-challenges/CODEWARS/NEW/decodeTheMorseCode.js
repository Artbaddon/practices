let decodeMorse = function (morseCode) {
  let trimmedCode = morseCode.trim();
  let words = trimmedCode.split("   ");
  console.log(words);
  let letters = words.map((ele) => ele.split(" "));
  let decodedWordsArr = [];

  for (let i = 0; i < letters.length; i++) {
    decodedWordsArr.push(letters[i].map((ele) => MORSE_CODE[ele]));
  }

  return decodedWordsArr.map((ele) => ele.join("")).join(" ");
};

